"""Набросок структуры программы и API для автоматического повторения ручных корректировок данных, преимущественно строковых.
    Полезно для тех алгоритмов, которые регулярно обрабатывают одни и те же / сильно похожие данные.

    АРХИТЕКТУРА МНОГИХ-КО-МНОГИМ:
    
    Концепция поддерживает архитектуру многих-ко-многим между слоем входных данных и слоем корректировок:
    
    - Разные входные данные (Item) могут разрешаться в один и тот же корректный логический объект (CorrectObject), 
      таким образом, кластеризуясь вокруг корректного значения через механизм кластеров (Cluster).
    
    - Один и тот же входной текст, но с разными контекстами, может разрешаться в разные кластеры 
      (и соответственно в разные корректные объекты). Выбор должен быть однозначным на основе обязательных 
      элементов контекста кластера.
		! ВАЖНО! Разрешение одного и того же входного объекта в разные кластеры с разными корректными объектами на практике ВОЗМОЖНО (может произойти), но это означает, что настройки кластеров некорректны, например, недостаточно строгие. Такие ситуации нужно обрабатывать отдельно, т.к. они требуют вмешательства человека, для того чтобы полностью автономный режим снова вел себя предсказуемо.

    ДВУХСЛОЙНАЯ МОДЕЛЬ:
    
    Существует два слоя:
    
    1. Слой входных данных — содержит множество Item (варианты написания с контекстом). entry, occurrence, необработанные, несвязанные данные.
    2. Слой корректировок — содержит CorrectObject (корректные логические объекты с ID из внешних справочников) 
       и Cluster (кластеры, связывающие CorrectObject с множеством Item).
       Resolution, Correction.
    
    Эти слои сопоставляются между собой через кластеры. Кластер хранит единый набор обязательных элементов 
    контекста (required_context_elements), по которому определяется принадлежность входящего Item к кластеру.
    
    ОБРАБОТКА НЕОДНОЗНАЧНОСТЕЙ И НЕРАЗРЕШЕННОСТЕЙ:
    
    - Неоднозначности: один и тот же текст с разными обязательными элементами контекста попадает в разные кластеры.
      Это обрабатывается через проверку соответствия обязательных элементов контекста кластера элементам контекста Item.
    
    - Неразрешенности: отклонения от один-к-одному обрабатываются через операции над кластерами:
      * Разделение кластера на два с разными корректными объектами
      * Слияние кластеров в один, где останется только один корректный объект

    При отстутствии в БД подходящего "эталонного" объекта, из входящего Item будет создан новый корректный объект, т.е. без изменения содержимого, но с перспективой на возможность дальнейшего корректирования.
    
    ПРЕДОТВРАЩЕНИЕ ДУБЛИКАТОВ корректных объектов:
    
    Механизм проверки уникальности корректных объектов (CorrectObject):
    - При наличии ID из внешнего справочника: CorrectObject с одинаковыми ID считаются одним и тем же объектом, дубликаты не допускаются.
    - При отсутствии ограничения по элементам контекста (required_context_elements пуст или отсутствует): 
      уникальность корректных объектов определяется по их текстовому значению (value становится ключом).
      CorrectObject с одинаковыми value считаются одним и тем же объектом.
    - При создании нового CorrectObject функция find_or_create_correct_object() сначала ищет существующий объект:
      * Если указан ID - ищет по ID.
      * Если ID не указан и отсутствуют ограничения по элементам контекста - ищет по value.
    - Если объект с таким ключом (ID или value) уже существует, возвращается существующий, даже если метаданные (name, description) отличаются.
    - Это гарантирует, что в системе не будет дубликатов корректных объектов с одинаковыми ключами.
    
    Логика предотвращения создания дубликатов кластеров:
    - Кластеры проверяются на дубликаты по комбинации CorrectObject и обязательных элементов контекста.
    - При поиске кластера для нового Item проверяется соответствие обязательных элементов контекста.
    - Если обязательные элементы контекста отсутствуют (пустой список), уникальность кластера определяется только по CorrectObject.
    - Если найден кластер с тем же CorrectObject и теми же обязательными элементами контекста (или оба без обязательных элементов) - используется существующий кластер.
    - Это предотвращает создание множественных кластеров для одного и того же логического объекта с одинаковыми критериями принадлежности.
    
    Стратегия разрешения конфликтов при создании объектов с одинаковыми ключами:
    - Если при создании CorrectObject обнаружен существующий объект с таким же ключом (ID или value) - используется существующий.
    - Метаданные существующего объекта сохраняются, новые метаданные игнорируются (или могут быть обновлены через отдельную операцию).
    - Это обеспечивает целостность данных и предотвращает потерю информации о корректных объектах.
    

    Комментарии к визуальной части:
    Список (таблица) Кластеров — основная web-страница для человека (администратора, ревьювера).
    
    
     — Состав строки про один Кластер:
     
        - Корректный объект (CorrectObject) — отображается ID и название (если доступно).
        
        - Обязательные элементы контекста — отображается список обязательных элементов контекста кластера 
          (ключ-значение), по которым определяется принадлежность Item к кластеру.
        
        - Облако вариантов написания — отображается количество входных Item в кластере, 
          при клике раскрывается список всех вариантов написания (можно показать первые несколько и "ещё N").
        
        - (значок "преобразования" из HTML-entities): 
            • знак равенства, если корректировка совпадает с самим заменяемым объектом,
            • прямая стрелка вправо, если корректировка взята из фиксированной гипотезы,
            • карандаш (или рука с карандашом), если корректировка взята из вручную редактируемой гипотезы,
            • перечеркнутая стрелка вправо для аннулированной гипотезы.

        - Гипотезы — отображается количество гипотез, при клике раскрывается список.
        
        - кнопки в виде иконок для выполнения действий:
            • подтверждение (галочка ОК) — если статус "ожидает",
            • редактирование (шестерёнка),
            • аннулирование (перечёркнутый круг СТОП / перечёркнутый глаз / др. со значением скрыть/заблокировать),
            • удаление (крестик или корзина),
            • разделение кластера (иконка разделения) — для разделения кластера на два,
            • слияние кластеров (иконка слияния) — для слияния с другим кластером.

        - дата создания и последней модификации.
        
     — Статус кластера влияет на цвет строки в таблице: ожидает: красный, подтверждён: чёрный (или неяркий зелёный), аннулирован: серый.
        (цвет может быть применён к фону или к шрифту)

     — Сортировка через заголовок таблицы по:
        - алфавиту по ID корректного объекта,
        - количеству вариантов написания,
        - дате создания,
        - дате изменения.

     — Фильтрация по статусу (ожидает, подтверждён, аннулирован — множественный выбор): чтобы можно было вывести в начало таблицы строки с любым статусом, а остальные скрыть.
     
     
    Форма редактирования Кластера.

    Может быть выполнена в виде модального окна или отдельной страницы.
    
    Основные разделы формы:
    - Корректный объект: отображение и редактирование CorrectObject (ID, название, описание).
    - Обязательные элементы контекста: список обязательных элементов контекста с возможностью добавления/удаления/редактирования.
      При первом сохранении внимание пользователя необходимо концентрировать на осознанном изучении и разметке этих элементов.
    - Облако вариантов написания: список всех входных Item в кластере с возможностью просмотра контекста каждого.
    - Гипотезы: список гипотез с возможностью добавления, редактирования, удаления, выбора основной (approved).
    
    Необходимые изменения в структуре содержимого формы (добавление и удаление гипотез, элементов контекста, вариантов написания) 
    должны выполняться по нажатию кнопок здесь же, без обращения к серверу.
    Для сохранения изменений внизу должна быть предусмотрена кнопка, как в обычной форме. Сохранение каждого нажатия в асинхронном режиме не требуется.

    Важный визуальный элемент — моноширинный текст и подсветка невидимых символов (пробелов) для смыслового 
    (изменяемого, заменяющего и редактируемого) текста, как например, это сделано здесь: https://den1s0v.github.io/regex-helper/src/.
    
    Операции над кластерами:
    - Разделение кластера: позволяет выбрать часть Item и создать новый кластер с другим CorrectObject и обязательными элементами контекста.
    - Слияние кластеров: позволяет объединить два кластера в один, выбрав целевой CorrectObject и обязательные элементы контекста.

    
    ОПИСАНИЕ РАБОТЫ КЛАСТЕРА:
    
    Кластер управляет статусами (PENDING, APPROVED, INVALID) аналогично бывшему классу Correction.
    
    Кластер хранит множество входных Item (варианты написания), которые кластеризуются вокруг CorrectObject.
    Это позволяет группировать различные варианты написания одного и того же логического объекта.
    
    Ключевой механизм определения принадлежности: единый набор обязательных элементов контекста (required_context_elements).
    - При создании кластера обязательные элементы контекста определяются на основе важных элементов контекста первого входного Item
      (все элементы контекста с important=True становятся обязательными).
    - Если важных элементов контекста нет (или они не были помечены как важные), required_context_elements может быть пустым.
    - Все последующие Item проверяются на соответствие этому набору обязательных элементов контекста.
    - Если обязательные элементы контекста Item совпадают с обязательными элементами кластера - Item может быть включен в кластер.
    - Это позволяет обрабатывать неоднозначности: один и тот же текст с разными обязательными элементами контекста попадает в разные кластеры.
    - При отсутствии обязательных элементов контекста (required_context_elements пуст): 
      уникальность корректных объектов определяется по их текстовому значению (value становится ключом).
      В этом случае кластеры с одинаковым CorrectObject (по value) считаются дубликатами и должны быть объединены.
    
    Кластер управляет гипотезами (фиксированные из запросов и созданные вручную через UI):
    - Фиксированные гипотезы приходят из запросов на корректировку через функцию apply_correction().
    - Дополнительные гипотезы создаются вручную через UI и имеют признак suggested_by_reviewer = True.
    - Гипотезы могут использоваться для промежуточного результата или для уточнения корректировки.
    - В конечном итоге корректировка возвращает CorrectObject кластера.
    
    Кластер выбирает оптимальную гипотезу на основе score и approved флага:
    - В статусе PENDING: возвращается CorrectObject (или гипотеза с максимальным score, если используется промежуточный результат).
    - В статусе APPROVED: возвращается CorrectObject (или выбранная человеком гипотеза с approved = True).
    - В статусе INVALID: не возвращает корректировку, но при запросе пересоздаётся со статусом PENDING.
    
    Жизненный цикл кластера:
    1. Создание: кластер создаётся со статусом PENDING при первом запросе корректировки для нового Item.
    2. Наполнение: в кластер добавляются новые Item и гипотезы по мере поступления запросов.
    3. Утверждение: человек проверяет кластер, настраивает обязательные элементы контекста, выбирает гипотезы, утверждает (статус APPROVED).
    4. Использование: утверждённый кластер используется для автоматической корректировки входящих Item.
    5. Обновление: при поступлении новых гипотез с более высоким score кластер может вернуться в статус PENDING для повторной проверки.
    6. Аннулирование: кластер может быть аннулирован (статус INVALID) и пересоздан при следующем запросе.

"""

from typing import Optional
from datetime import datetime


class Item:
    """Корректируемый объект/сущность (или гипотеза — вариант замены корректируемого объекта).
        Корректируемый объект выражается строкой `value`.
        Может иметь контекст, содержимое которого может повлиять на выбор человека в пользу той или иной гипотезы,
        а также на определение принадлежности Item к кластеру через проверку соответствия обязательным элементам контекста.
        
        Объекты `Item` с одинаковыми `value`, но с разными контекстами считаются разными, если есть отличия 
        в составе либо значениях важных элементов контекста, и одинаковыми, если важные элементы контекста 
        полностью совпадают (отличия в неважных элементах игнорируются).
        
        Item может быть связан с кластером через поле `cluster_id`, что указывает на принадлежность к конкретному кластеру.
    """
    value: str  # Коректируемое значение.

    context: list['ContextElement']  # Набор значений "контекста" — дополнительная информация произвольного вида, 
                                      # по которой человек может догадаться о принадлежности объекта и сделать выбор 
                                      # в пользу той или иной гипотезы. Также используется для определения принадлежности 
                                      # Item к кластеру через проверку соответствия обязательным элементам контекста кластера. 
                                      # Может быть пустым.
    
    cluster_id: Optional[int] = None  # Идентификатор кластера, к которому принадлежит этот Item (опционально). 
                                       # Устанавливается при включении Item в кластер.
    
    score: Optional[float] = 0  # Степень совпадения гипотезы с корректируемым объектом. Диапазон: [0..1], 
                                 # значение больше — совпадение лучше. Имеет смысл для `Item` в качестве гипотезы; 
                                 # для самого корректируемого объекта `score` принимается равным 0 
                                 # (как наименее правдоподобная гипотеза: если для объекта была запрошена коррекция, 
                                 # то «не корректировать вовсе» будет последним вариантом).
    
    # is_default: bool — избыточен при наличии `score`.
    
    approved: bool = False  # флаг: "одобрено", "выбрано в качестве желательного варианта для корректировки"

    suggested_by_reviewer: bool = False  # флаг, позволяющий указать, что вариант замены был создан вручную проверяющим человеком 
                                          # (когда ни одна из гипотез, по его мнению, не подошла, и он внёс руками свой вариант) 
                                          # и может быть отредактирован вручную.


class CorrectObject(Item):
    """Корректный логический объект из слоя корректировок.
        Наследуется от Item, представляя собой нормализованное, корректное значение, которое является результатом корректировки.
        Связан со справочниками из внешнего источника через уникальный ID (если доступен).
        Используется как ядро кластера - множество вариантов написания (Item) кластеризуются вокруг одного CorrectObject.
        
        Наследует все свойства Item (value, context, score, approved, suggested_by_reviewer, cluster_id),
        добавляя специфичные для корректного объекта свойства (id, name, description).
        
        Поле `value` из Item используется для хранения строкового представления корректного объекта.
        Поле `id` содержит уникальный идентификатор из внешнего справочника (опционально).
        
        Уникальность объекта определяется по ключу:
        - Если указан `id` - уникальность определяется по ID (CorrectObject с одинаковыми ID считаются одним объектом).
        - Если `id` не указан и отсутствуют ограничения по элементам контекста - уникальность определяется по `value` 
          (CorrectObject с одинаковыми value считаются одним объектом, value становится ключом).
        Дубликаты не допускаются.
    """
    id: Optional[str] = None  # Уникальный идентификатор из внешнего справочника (опционально, например, строковый ID во внешней системе).
    
    # Метаданные (опциональные):
    name: Optional[str] = None  # Название объекта (если доступно из справочника).
    description: Optional[str] = None  # Описание объекта (если доступно из справочника).
    # Дополнительные метаданные могут быть добавлены по необходимости.


class ContextElement:
    """Элемент контекста: Информация произвольного вида, предназначенная для просмотра человеком. 
        По своей структуре «ключ-значение» больше всего напоминает элемент словаря.
        Дополнительный флаг `important` служит для пометки элемента контекста как важного (True) либо несущественного (False) при определении наиболее подходящей гипотезы.
    """
    key: str  # ключ элемента контекста
    value: str  # значение элемента контекста
    important: bool = False  # признак значимости для принятия решения о выбранной гипотезе (задаётся человеком через UI).


class Cluster:
    """Кластер: изменяемый способ откорректировать множество входных объектов.
        ЗАМЕНЯЕТ класс Correction. Представляет кластер: корректный объект + облако вариантов написания.
        
        Ключевая особенность: единый набор обязательных элементов контекста (required_context_elements).
        Все входящие кандидаты (Item) проверяются на соответствие этому набору обязательных элементов контекста.
        Это позволяет определять, подходит ли новый Item для включения в кластер, и обрабатывать неоднозначности:
        один и тот же текст с разными обязательными элементами контекста может попадать в разные кластеры.
        
        Кластер связывает один CorrectObject с множеством входных Item (варианты написания).
        Отображается в UI и может быть в любой момент отредактирован человеком, а также аннулирован в случае устаревания.
        
        Процесс корректировки объекта состоит в том, чтобы получить вместо него CorrectObject кластера.
        Гипотезы `hypotheses` по происхождению делятся на фиксированные (пришедшие из запроса на корректировку) 
        и дополнительные, созданные и редактируемые вручную через UI (у последних признак `suggested_by_reviewer` = True).

        Создаётся со статусом PENDING и ожидает проверки и утверждения человеком.
        В статусе PENDING:
         — высвечивается человеку как непроверенная.
         — отдаёт в качестве оптимальной корректировки CorrectObject (или гипотезу с максимальным `score`, 
           если гипотезы используются для промежуточного результата).
         — Может быть утверждена (см. далее).

        При утверждении/редактировании человеком:
         — обязательные элементы контекста могут быть изменены (на основе важных элементов контекста входных Item).
           При первом сохранении внимание пользователя необходимо концентрировать на осознанном изучении и разметке 
           этих элементов контекста (если ни один элемент важным не является, то пусть выберет доп. пункт 
           «ни один пункт не важен»; но нельзя разрешать совсем пустой выбор).
         — статус изменяется на APPROVED (или на INVALID, если нужно вывести кластер из эксплуатации без полного удаления).
         — одна из гипотез может получить `approved` = True (не более одной, т.е. single-selection в стиле radio-buttons), 
           в случае же сохранения без утверждения какой-либо гипотезы, утверждённой автоматически становится оптимальная, 
           как в статусе PENDING (см. выше).
         — могут быть удалены фиксированные гипотезы (человек посчитал их полностью неадекватными), 
           хотя это и не должно считаться нормальной практикой.
         — могут быть добавлены, изменены или удалены дополнительные гипотезы, вводимые человеком вручную через 
           текстовое поле ввода (их `score` по умолчанию задаётся равным 1, как наиболее желательных). 
           Эти гипотезы участвуют в процессе наравне c фиксированными и могут быть выбраны как основные (`approved` = True). 
           Обычно имеет смысл создавать не более одной дополнительной гипотезы.
         — сохраняется время создания (обновления) кластера.
         
        В статусе APPROVED:
         — высвечивается человеку как проверенная.
         — отдаёт в качестве оптимальной корректировки CorrectObject (или выбранную человеком гипотезу с `approved` = True).
         — может быть отредактирован, а также переведён в статус INVALID.

        В статусе INVALID:
         — высвечивается человеку как аннулированный.
         — НЕ отдаёт оптимальную корректировку, но вместо этого ПРИ ЗАПРОСЕ "пересоздаётся" заново со статусом PENDING, 
           обнулив при этом `score` созданных вручную дополнительных гипотез и заменив фиксированные гипотезы на гипотезы
           из нового запроса на корректировку, а также отменив ручной выбор любых гипотез как предпочтительных 
           (таким образом, полностью повторяя поведение созданного с нуля кластера, но сохраняя ручной ввод безопасным образом).
         — может быть полностью удалён из системы.
         — может быть отредактирован и переведён в статус APPROVED.
    """
    correct_object: CorrectObject  # Корректный логический объект - ядро кластера.
    
    required_context_elements: list[ContextElement]  # Единый набор обязательных элементов контекста для проверки всех входящих кандидатов.
                                                      # Все Item проверяются на соответствие этому набору при определении принадлежности к кластеру.
    
    input_items: list[Item]  # Облако вариантов написания - множество входных Item, которые кластеризуются вокруг correct_object.
    
    hypotheses: list[Item]  # Набор гипотез о корректировке. Значение value выбранной гипотезы может использоваться 
                            # для промежуточного результата или для уточнения корректировки. 
                            # В конечном итоге корректировка возвращает correct_object.
    
    status: int = 0  # Статус кластера: 0 — PENDING: создан и требует проверки, 1 — APPROVED: проверен и утверждён, 2 — INVALID: аннулирован и отключён до следующего востребования.

    # метаданные:
    created_at: datetime
    updated_at: datetime
    scope_id: int = 0  # Указание на предметную область, в рамках которой имеет смысл искать имеющиеся кластеры. Предметные области не пересекаются между собой.


