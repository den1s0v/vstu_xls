"""Набросок структуры программы и API для автоматического повторения ручных корректировок данных, преимущественно строковых.
    Полезно для тех алгоритмов, которые регулярно обрабатывают одни и те же / сильно похожие данные.

    АРХИТЕКТУРА РАЗРЕШЕНИЯ ВХОЖДЕНИЙ:
    
    Концепция поддерживает связь между слоем входных данных и слоем корректировок через механизм разрешений:
    
    - Разные входные данные (Occurrence) могут разрешаться в один и тот же корректный логический объект (CorrectObject) 
      через разные Resolution (разрешения).
    
    - Один и тот же входной текст, но с разными контекстами, может разрешаться в разные Resolution 
      (и соответственно в разные корректные объекты). Выбор должен быть однозначным на основе обязательных 
      элементов контекста Resolution.
    ! ВАЖНО! Разрешение одного и того же входного объекта (Occurrence) в разные Resolution с разными корректными объектами 
    на практике ВОЗМОЖНО (может произойти), но это означает, что настройки Resolution некорректны, например, недостаточно строгие. 
    Такие ситуации нужно обрабатывать отдельно, т.к. они требуют вмешательства человека, для того чтобы полностью автономный 
    режим снова вел себя предсказуемо.

    ДВУХСЛОЙНАЯ МОДЕЛЬ:
    
    Существует два слоя:
    
    1. Слой входных данных — содержит множество Occurrence (вхождения, варианты написания с контекстом). 
       Entry, occurrence, необработанные, несвязанные данные.
    2. Слой корректировок — содержит CorrectObject (корректные логические объекты с ID из внешних справочников) 
       и Resolution (разрешения, связывающие Occurrence с CorrectObject).
       Resolution, Correction.
    
    Эти слои сопоставляются между собой через Resolution. CorrectObject хранит единый набор обязательных элементов 
    контекста (required_context_elements), по которому определяется соотнесение/привязка входящего Occurrence к CorrectObject.
    Resolution не создается между Occurrence и CorrectObject, если нет совпадения по обязательным элементам контекста.
    
    Связи:
    - Occurrence → Resolution: один-ко-многим (одно вхождение может иметь несколько разрешений)
    - Resolution → CorrectObject: многие-к-одному (несколько разрешений могут указывать на один корректный объект)
    
    ОБРАБОТКА НЕОДНОЗНАЧНОСТЕЙ И НЕРАЗРЕШЕННОСТЕЙ:
    
    - Неоднозначности: один и тот же текст с разными обязательными элементами контекста попадает в разные CorrectObject.
      Это обрабатывается через проверку соответствия обязательных элементов контекста CorrectObject элементам контекста Occurrence.
      Resolution не создается, если нет совпадения по обязательным элементам контекста (с учетом absence_allowed).
    
    - Неразрешенности: отклонения от один-к-одному обрабатываются через операции над Resolution:
      * Разделение Resolution на два с разными корректными объектами
      * Слияние Resolution в один, где останется только один корректный объект

    При отстутствии в БД подходящего "эталонного" объекта, из входящего Item будет создан новый корректный объект, т.е. без изменения содержимого, но с перспективой на возможность дальнейшего корректирования.
    
    ПРЕДОТВРАЩЕНИЕ ДУБЛИКАТОВ корректных объектов:
    
    Механизм проверки уникальности корректных объектов (CorrectObject):
    - При наличии ID из внешнего справочника: CorrectObject с одинаковыми ID считаются одним и тем же объектом, дубликаты не допускаются.
    - При отсутствии ограничения по элементам контекста (required_context_elements пуст или отсутствует): 
      уникальность корректных объектов определяется по их текстовому значению (value становится ключом).
      CorrectObject с одинаковыми value считаются одним и тем же объектом.
    - При создании нового CorrectObject функция find_or_create_correct_object() сначала ищет существующий объект:
      * Если указан ID - ищет по ID.
      * Если ID не указан и отсутствуют ограничения по элементам контекста - ищет по value.
    - Если объект с таким ключом (ID или value) уже существует, возвращается существующий, даже если метаданные (name, description) отличаются.
    - Это гарантирует, что в системе не будет дубликатов корректных объектов с одинаковыми ключами.
    
    Логика предотвращения создания дубликатов Resolution:
    - Resolution проверяются на дубликаты по комбинации Occurrence и CorrectObject.
    - При поиске Resolution для нового Occurrence проверяется соответствие обязательных элементов контекста CorrectObject элементам контекста Occurrence.
    - Resolution не создается, если нет совпадения по обязательным элементам контекста (с учетом absence_allowed в ContextElement).
    - Если обязательные элементы контекста в CorrectObject отсутствуют (пустой список), Resolution может быть создано для любого Occurrence.
    - Если для пары (Occurrence, CorrectObject) уже существует Resolution - используется существующее.
    - Это предотвращает создание множественных Resolution для одной и той же пары (Occurrence, CorrectObject).
    
    Стратегия разрешения конфликтов при создании объектов с одинаковыми ключами:
    - Если при создании CorrectObject обнаружен существующий объект с таким же ключом (ID или value) - используется существующий.
    - Метаданные существующего объекта сохраняются, новые метаданные игнорируются (или могут быть обновлены через отдельную операцию).
    - Это обеспечивает целостность данных и предотвращает потерю информации о корректных объектах.
    

    Комментарии к визуальной части:
    Список (таблица) Resolution — основная web-страница для человека (администратора, ревьювера).
    
    
     — Состав строки про одно Resolution:
     
        - Вхождение (Occurrence) — отображается value и контекст вхождения.
        
        - Корректный объект (CorrectObject) — отображается ID и название (если доступно).
        
        - Обязательные элементы контекста — отображается список обязательных элементов контекста CorrectObject 
          (ключ-значение, absence_allowed), по которым определяется возможность создания Resolution между Occurrence и CorrectObject.
        
        - (значок "преобразования" из HTML-entities): 
            • знак равенства, если корректировка совпадает с самим заменяемым объектом,
            • прямая стрелка вправо, если Resolution автоматическое,
            • карандаш (или рука с карандашом), если Resolution создано вручную (manual=True),
            • перечеркнутая стрелка вправо для аннулированного Resolution.

        - кнопки в виде иконок для выполнения действий:
            • подтверждение (галочка ОК) — если статус "ожидает",
            • редактирование (шестерёнка),
            • аннулирование (перечёркнутый круг СТОП / перечёркнутый глаз / др. со значением скрыть/заблокировать),
            • удаление (крестик или корзина),
            • разделение Resolution (иконка разделения) — для создания нового Resolution с другим CorrectObject,
            • слияние Resolution (иконка слияния) — для слияния с другим Resolution.

        - дата создания и последней модификации.
        
     — Статус Resolution влияет на цвет строки в таблице: ожидает: красный, подтверждён: чёрный (или неяркий зелёный), аннулирован: серый.
        (цвет может быть применён к фону или к шрифту)

     — Сортировка через заголовок таблицы по:
        - алфавиту по value вхождения,
        - алфавиту по ID корректного объекта,
        - дате создания,
        - дате изменения.

     — Фильтрация по статусу (ожидает, подтверждён, аннулирован — множественный выбор): чтобы можно было вывести в начало таблицы строки с любым статусом, а остальные скрыть.
     
     
    Форма редактирования Resolution.

    Может быть выполнена в виде модального окна или отдельной страницы.
    
    Основные разделы формы:
    - Вхождение: отображение Occurrence (value, context) - только для просмотра.
    - Корректный объект: отображение и редактирование CorrectObject (ID, название, описание).
    - Обязательные элементы контекста CorrectObject: список обязательных элементов контекста с возможностью добавления/удаления/редактирования.
      Для каждого элемента можно настроить absence_allowed (разрешено ли отсутствие элемента во входящем объекте).
      При первом сохранении внимание пользователя необходимо концентрировать на осознанном изучении и разметке этих элементов.
    
    Необходимые изменения в структуре содержимого формы (добавление и удаление элементов контекста, настройка absence_allowed) 
    должны выполняться по нажатию кнопок здесь же, без обращения к серверу.
    Для сохранения изменений внизу должна быть предусмотрена кнопка, как в обычной форме. Сохранение каждого нажатия в асинхронном режиме не требуется.

    Важный визуальный элемент — моноширинный текст и подсветка невидимых символов (пробелов) для смыслового 
    (изменяемого, заменяющего и редактируемого) текста, как например, это сделано здесь: https://den1s0v.github.io/regex-helper/src/.
    
    Операции над Resolution:
    - Разделение Resolution: позволяет создать новое Resolution для того же Occurrence с другим CorrectObject 
      (обязательные элементы контекста берутся из нового CorrectObject).
    - Слияние Resolution: позволяет объединить два Resolution в одно, выбрав целевой CorrectObject 
      (обязательные элементы контекста берутся из целевого CorrectObject).

    
    ОПИСАНИЕ РАБОТЫ RESOLUTION:
    
    Resolution управляет статусами (PENDING, APPROVED, INVALID) аналогично бывшему классу Correction.
    
    Resolution связывает Occurrence (вхождение) с CorrectObject (корректный объект).
    Связи: Occurrence → Resolution (один-ко-многим), Resolution → CorrectObject (многие-к-одному).
    Это позволяет одному вхождению иметь несколько способов разрешения, а нескольким разрешениям указывать на один корректный объект.
    
    Ключевой механизм определения принадлежности: единый набор обязательных элементов контекста (required_context_elements) хранится в CorrectObject.
    - При создании CorrectObject обязательные элементы контекста определяются на основе важных элементов контекста входного Occurrence
      (все элементы контекста с important=True становятся обязательными, absence_allowed=False по умолчанию).
    - Если важных элементов контекста нет (или они не были помечены как важные), required_context_elements может быть пустым.
    - Все последующие Occurrence проверяются на соответствие обязательным элементам контекста CorrectObject.
    - Resolution НЕ создается между Occurrence и CorrectObject, если нет совпадения по обязательным элементам контекста.
    - Проверка соответствия:
      * Для каждого обязательного элемента контекста в CorrectObject:
        - Если элемент присутствует в контексте Occurrence и значения совпадают - элемент соответствует
        - Если элемент отсутствует в контексте Occurrence:
          * Если absence_allowed=True для этого элемента - элемент соответствует (разрешено отсутствие, "тип" может быть неизвестен заранее)
          * Если absence_allowed=False для этого элемента - элемент НЕ соответствует (обязательно должен быть указан, "тип" должен быть известен заранее)
      * Если все обязательные элементы соответствуют - можно создать Resolution
    - Это позволяет обрабатывать неоднозначности: один и тот же текст с разными обязательными элементами контекста попадает в разные CorrectObject.
    - При отсутствии обязательных элементов контекста (required_context_elements пуст): 
      уникальность корректных объектов определяется по их текстовому значению (value становится ключом).
      В этом случае Resolution с одинаковым CorrectObject (по value) могут указывать на один и тот же объект.
    
    Resolution возвращает CorrectObject:
    - В статусе PENDING: возвращается CorrectObject (ожидает проверки человеком).
    - В статусе APPROVED: возвращается CorrectObject (проверено и утверждено).
    - В статусе INVALID: не возвращает корректировку, но при запросе пересоздаётся со статусом PENDING.
    
    Жизненный цикл Resolution:
    1. Создание: Resolution создаётся со статусом PENDING при первом запросе корректировки для нового Occurrence, 
       если найдено соответствие по обязательным элементам контекста CorrectObject.
    2. Утверждение: человек проверяет Resolution, настраивает обязательные элементы контекста в CorrectObject, утверждает (статус APPROVED).
    3. Использование: утверждённое Resolution используется для автоматической корректировки входящих Occurrence.
    4. Аннулирование: Resolution может быть аннулировано (статус INVALID) и пересоздано при следующем запросе.

"""

from typing import Optional
from datetime import datetime


class Item:
    """Базовый класс для корректируемых объектов/сущностей.
        Корректируемый объект выражается строкой `value`.
        Может иметь контекст, содержимое которого может повлиять на выбор человека в пользу той или иной гипотезы,
        а также на определение принадлежности к Resolution через проверку соответствия обязательным элементам контекста.
        
        Объекты `Item` с одинаковыми `value`, но с разными контекстами считаются разными, если есть отличия 
        в составе либо значениях важных элементов контекста, и одинаковыми, если важные элементы контекста 
        полностью совпадают (отличия в неважных элементах игнорируются).
        
        Базовый класс для Occurrence (вхождения) и CorrectObject (корректных объектов).
    """
    value: str  # Коректируемое значение.

    context: list['ContextElement']  # Набор значений "контекста" — дополнительная информация произвольного вида, 
                                      # по которой человек может догадаться о принадлежности объекта и сделать выбор 
                                      # в пользу той или иной гипотезы. Также используется для определения принадлежности 
                                      # Occurrence к Resolution через проверку соответствия обязательным элементам контекста Resolution. 
                                      # Может быть пустым.
    
    score: Optional[float] = 0  # Степень совпадения гипотезы с корректируемым объектом. Диапазон: [0..1], 
                                 # значение больше — совпадение лучше. Имеет смысл для `Item` в качестве гипотезы; 
                                 # для самого корректируемого объекта `score` принимается равным 0 
                                 # (как наименее правдоподобная гипотеза: если для объекта была запрошена коррекция, 
                                 # то «не корректировать вовсе» будет последним вариантом).
    
    # is_default: bool — избыточен при наличии `score`.
    
    approved: bool = False  # флаг: "одобрено", "выбрано в качестве желательного варианта для корректировки"

    suggested_by_reviewer: bool = False  # флаг, позволяющий указать, что вариант замены был создан вручную проверяющим человеком 
                                          # (когда ни одна из гипотез, по его мнению, не подошла, и он внёс руками свой вариант) 
                                          # и может быть отредактирован вручную.


class Occurrence(Item):
    """Вхождение — входной объект из слоя входных данных.
        Наследуется от Item, представляя собой конкретное вхождение текстового значения в исходных данных.
        Это необработанные, несвязанные данные (entry, occurrence), которые требуют разрешения/корректировки.
        
        Наследует все свойства Item (value, context, score, approved, suggested_by_reviewer).
        Поле `value` содержит исходное текстовое значение вхождения.
        Поле `context` содержит контекст вхождения, который используется для определения подходящего Resolution.
        
        Occurrence может иметь несколько Resolution (один-ко-многим), каждое из которых разрешает его в CorrectObject.
    """
    status: Optional[int] = None  # Статус вхождения (опционально, для отслеживания состояния обработки).


class CorrectObject(Item):
    """Корректный логический объект из слоя корректировок.
        Наследуется от Item, представляя собой нормализованное, корректное значение, которое является результатом корректировки.
        Связан со справочниками из внешнего источника через уникальный ID (если доступен).
        Используется как целевой объект для Resolution - множество вхождений (Occurrence) разрешаются в CorrectObject через Resolution.
        
        Наследует все свойства Item (value, context, score, approved, suggested_by_reviewer),
        добавляя специфичные для корректного объекта свойства (id, name, description, required_context_elements).
        
        Поле `value` из Item используется для хранения строкового представления корректного объекта.
        Поле `id` содержит уникальный идентификатор из внешнего справочника (опционально).
        
        Ключевая особенность: единый набор обязательных элементов контекста (required_context_elements).
        Все входящие кандидаты (Occurrence) проверяются на соответствие этому набору обязательных элементов контекста.
        Это позволяет определять, подходит ли новое Occurrence для создания Resolution с этим CorrectObject, и обрабатывать неоднозначности:
        один и тот же текст с разными обязательными элементами контекста может попадать в разные CorrectObject.
        
        Resolution не создается между Occurrence и CorrectObject, если нет совпадения по обязательным элементам контекста.
        Если во входном объекте обязательный элемент контекста не указан, то дальнейшее поведение зависит от строгости настроек 
        элемента контекста в CorrectObject:
        - Если элемент контекста разрешает отсутствие (absence_allowed=True), то можно создать Resolution 
          (предполагаем, что объект может принадлежать этому "типу", если "тип" неизвестен заранее).
        - Если элемент контекста не разрешает отсутствие (absence_allowed=False), то Resolution не создается 
          ("тип" обязательно должен быть известен заранее).
        
        Уникальность объекта определяется по ключу:
        - Если указан `id` - уникальность определяется по ID (CorrectObject с одинаковыми ID считаются одним объектом).
        - Если `id` не указан и отсутствуют ограничения по элементам контекста - уникальность определяется по `value` 
          (CorrectObject с одинаковыми value считаются одним объектом, value становится ключом).
        Дубликаты не допускаются.
        
        Resolution → CorrectObject: многие-к-одному (несколько разрешений могут указывать на один корректный объект).
    """
    id: Optional[str] = None  # Уникальный идентификатор из внешнего справочника (опционально, например, строковый ID во внешней системе).
    
    required_context_elements: list[ContextElement]  # Единый набор обязательных элементов контекста для проверки всех входящих кандидатов.
                                                     # Все Occurrence проверяются на соответствие этому набору при определении возможности создания Resolution.
                                                     # Может быть пустым (если нет ограничений по контексту).
    
    # Метаданные (опциональные):
    name: Optional[str] = None  # Название объекта (если доступно из справочника).
    description: Optional[str] = None  # Описание объекта (если доступно из справочника).
    # Дополнительные метаданные могут быть добавлены по необходимости.


class ContextElement:
    """Элемент контекста: Информация произвольного вида, предназначенная для просмотра человеком. 
        По своей структуре «ключ-значение» больше всего напоминает элемент словаря.
        Дополнительный флаг `important` служит для пометки элемента контекста как важного (True) либо несущественного (False) при определении наиболее подходящей гипотезы.
        Флаг `absence_allowed` определяет, разрешено ли отсутствие этого элемента контекста во входящем объекте при проверке соответствия.
    """
    key: str  # ключ элемента контекста
    value: str  # значение элемента контекста
    important: bool = False  # признак значимости для принятия решения о выбранной гипотезе (задаётся человеком через UI).
    absence_allowed: bool = False  # разрешено ли отсутствие этого элемента контекста во входящем объекте при проверке соответствия.
                                   # Если True - объект может принадлежать этому "типу", даже если элемент контекста не указан ("тип" может быть неизвестен заранее).
                                   # Если False - элемент контекста обязательно должен быть указан ("тип" обязательно должен быть известен заранее).


class Resolution:
    """Разрешение: способ разрешения вхождения (Occurrence) в корректный объект (CorrectObject).
        Представляет способ откорректировать входное вхождение, связывая его с корректным объектом.
        
        Resolution связывает Occurrence с CorrectObject.
        Связи: Occurrence → Resolution (один-ко-многим), Resolution → CorrectObject (многие-к-одному).
        Отображается в UI и может быть в любой момент отредактирован человеком, а также аннулирован в случае устаревания.
        
        Процесс корректировки вхождения состоит в том, чтобы получить вместо него CorrectObject через Resolution.
        
        Resolution НЕ создается между Occurrence и CorrectObject, если нет совпадения по обязательным элементам контекста.
        Обязательные элементы контекста хранятся в CorrectObject (required_context_elements).
        Проверка соответствия:
        - Если во входном объекте обязательный элемент контекста не указан, то дальнейшее поведение зависит от строгости настроек 
          элемента контекста в CorrectObject:
          * Если элемент контекста разрешает отсутствие (absence_allowed=True), то можно создать Resolution 
            (предполагаем, что объект может принадлежать этому "типу", если "тип" неизвестен заранее).
          * Если элемент контекста не разрешает отсутствие (absence_allowed=False), то Resolution не создается 
            ("тип" обязательно должен быть известен заранее).
        
        Создаётся со статусом PENDING и ожидает проверки и утверждения человеком.
        В статусе PENDING:
         — высвечивается человеку как непроверенное.
         — отдаёт в качестве оптимальной корректировки CorrectObject.
         — Может быть утверждено (см. далее).

        При утверждении/редактировании человеком:
         — статус изменяется на APPROVED (или на INVALID, если нужно вывести Resolution из эксплуатации без полного удаления).
         — сохраняется время создания (обновления) Resolution.
         
        В статусе APPROVED:
         — высвечивается человеку как проверенное.
         — отдаёт в качестве оптимальной корректировки CorrectObject.
         — может быть отредактировано, а также переведено в статус INVALID.

        В статусе INVALID:
         — высвечивается человеку как аннулированное.
         — НЕ отдаёт оптимальную корректировку, но вместо этого ПРИ ЗАПРОСЕ "пересоздаётся" заново со статусом PENDING.
         — может быть полностью удалено из системы.
         — может быть отредактировано и переведено в статус APPROVED.
    """
    occurrence: Occurrence  # Вхождение, которое разрешается этим Resolution.
    
    correct_object: CorrectObject  # Корректный логический объект, в который разрешается вхождение.
    
    manual: bool = False  # Флаг, указывающий, что Resolution было создано вручную человеком (а не автоматически).
    
    status: int = 0  # Статус Resolution: 0 — PENDING: создано и требует проверки, 1 — APPROVED: проверено и утверждено, 2 — INVALID: аннулировано и отключено до следующего востребования.
    
    score: Optional[float] = 0  # Степень уверенности в правильности Resolution. Диапазон: [0..1], значение больше — уверенность выше.

    # метаданные:
    created_at: datetime
    updated_at: datetime
    scope_id: int = 0  # Указание на предметную область, в рамках которой имеет смысл искать имеющиеся Resolution. Предметные области не пересекаются между собой.


