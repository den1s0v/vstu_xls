# Ключевая функция API корректировок.
def apply_correction(
                     subject: Item, 
                     hypotheses: list[Item], 
                     scope_id: int = 0,
                    ) -> CorrectObject:
    """Возвращает оптимальную корректировку для заданного объекта, учитывая созданные вручную переопределения.
    
    Работает с кластерами вместо корректировок. Ищет существующий кластер по входному Item,
    проверяя соответствие обязательных элементов контекста кластера элементам контекста входного Item.
    
    Алгоритм:
        1. Найти существующий кластер по входному `Item`:
           - Проверить соответствие обязательных элементов контекста кластера (`required_context_elements`) 
             с элементами контекста входного `Item`
           - Если обязательные элементы совпадают - кластер подходит
           - Если найдено несколько подходящих кластеров - выбрать наиболее подходящий 
             (например, по количеству совпадающих элементов или по дате последнего обновления)
        
        2. Если кластер найден и в статусе APPROVED:
           - Проверить, нет ли в запросе гипотез с более высоким рейтингом `score`, чем текущие гипотезы кластера
           - Если есть: добавить новые гипотезы в кластер, сохранить его в БД и выставить статус PENDING
           - Вернуть `CorrectObject` найденного кластера
        
        3. Если кластер найден и в статусе PENDING:
           - Добавить новые гипотезы (если есть) в кластер
           - Добавить `subject` в `input_items` кластера (если ещё не добавлен)
           - Сохранить кластер в БД
           - Вернуть `CorrectObject` кластера (или оптимальную гипотезу, если используется промежуточный результат)
        
        4. Если кластер найден и в статусе INVALID:
           - Пересоздать кластер со статусом PENDING:
             * Заменить фиксированные гипотезы на гипотезы из нового запроса
             * Сохранить вручную созданные гипотезы, но обнулить их `score` и снять `approved`
             * Обновить `input_items` и `required_context_elements` при необходимости
           - Сохранить кластер в БД
           - Вернуть `CorrectObject` кластера
        
        5. Если кластер не найден - создать новый кластер со статусом PENDING:
           - Определить обязательные элементы контекста на основе важных элементов контекста входного `Item`
             (все элементы контекста с `important=True` становятся обязательными)
           - Создать или найти `CorrectObject`:
             * Если в гипотезах есть информация об ID корректного объекта - использовать `find_or_create_correct_object()` с указанием ID
             * Если ID нет, но обязательные элементы контекста отсутствуют (пустой список) - использовать `find_or_create_correct_object()` 
               для поиска по value (value становится ключом для уникальности)
             * Иначе создать новый `CorrectObject` (использовать значение из лучшей гипотезы или из subject)
           - Создать новый кластер с:
             * `correct_object` = найденный/созданный CorrectObject
             * `required_context_elements` = обязательные элементы контекста
             * `input_items` = [subject]
             * `hypotheses` = hypotheses из запроса
             * `status` = PENDING
           - Сохранить кластер в БД
           - Вернуть `CorrectObject` созданного кластера

    Args:
        subject (Item): Корректируемый объект.
        hypotheses (list[Item]): Перечень гипотез для коррекции (может быть пустым).
        scope_id (int, optional): Идентификатор области действия (нужен, чтобы разграничивать рабочие области 
                                  для разных клиентов нашего API). Defaults to 0 — global scope.

    Returns:
        CorrectObject: Корректный объект из найденного или созданного кластера.
    """
    pass
