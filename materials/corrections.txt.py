# Ключевая функция API корректировок.
def apply_correction(
                     occurrence: Occurrence, 
                     scope_id: int = 0,
                    ) -> CorrectObject:
    """Возвращает оптимальную корректировку для заданного вхождения, учитывая созданные вручную переопределения.
    
    Работает с Resolution. Ищет существующее Resolution по входному Occurrence,
    проверяя соответствие обязательных элементов контекста CorrectObject элементам контекста входного Occurrence.
    
    Алгоритм:
        1. Найти существующее Resolution по входному `Occurrence`:
           - Если найдено Resolution в статусе APPROVED - вернуть `CorrectObject` найденного Resolution
           - Если найдено Resolution в статусе PENDING - вернуть `CorrectObject` Resolution (ожидает проверки человеком)
           - Если найдено Resolution в статусе INVALID - пересоздать со статусом PENDING (см. шаг 5)
        
        2. Если Resolution не найдено - найти подходящий CorrectObject:
           - Для каждого существующего CorrectObject проверить соответствие обязательных элементов контекста:
             * Для каждого обязательного элемента контекста в CorrectObject:
               - Если элемент присутствует в контексте Occurrence и значения совпадают - элемент соответствует
               - Если элемент отсутствует в контексте Occurrence:
                 * Если absence_allowed=True для этого элемента - элемент соответствует (разрешено отсутствие)
                 * Если absence_allowed=False для этого элемента - элемент НЕ соответствует (обязательно должен быть указан)
             * Если все обязательные элементы соответствуют - CorrectObject подходит
           - Если найдено несколько подходящих CorrectObject - выбрать наиболее подходящий 
             (например, по количеству совпадающих элементов, по score, или по дате последнего обновления)
        
        3. Если подходящий CorrectObject найден:
           - Проверить, существует ли уже Resolution для пары (Occurrence, CorrectObject)
           - Если Resolution существует - использовать его (см. шаг 1)
           - Если Resolution не существует - создать новое Resolution со статусом PENDING (см. шаг 5)
        
        4. Если подходящий CorrectObject не найден:
           - Создать новый CorrectObject:
             * Определить обязательные элементы контекста на основе важных элементов контекста входного `Occurrence`
               (все элементы контекста с `important=True` становятся обязательными, absence_allowed=False по умолчанию)
             * Если обязательные элементы контекста отсутствуют (пустой список) - использовать `find_or_create_correct_object()` 
               для поиска по value (value становится ключом для уникальности)
             * Иначе создать новый `CorrectObject` с required_context_elements (использовать значение из occurrence.value)
           - Создать новое Resolution со статусом PENDING (см. шаг 5)
        
        5. Создание нового Resolution:
           - `occurrence` = occurrence
           - `correct_object` = найденный/созданный CorrectObject
           - `status` = PENDING
           - `manual` = False
           - Сохранить Resolution в БД
           - Вернуть `CorrectObject` созданного Resolution

    Args:
        occurrence (Occurrence): Вхождение, которое нужно откорректировать.
        scope_id (int, optional): Идентификатор области действия (нужен, чтобы разграничивать рабочие области 
                                  для разных клиентов нашего API). Defaults to 0 — global scope.

    Returns:
        CorrectObject: Корректный объект из найденного или созданного Resolution.
    """
    pass
